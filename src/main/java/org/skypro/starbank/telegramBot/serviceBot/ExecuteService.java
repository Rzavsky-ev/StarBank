package org.skypro.starbank.telegramBot.serviceBot;

import com.pengrad.telegrambot.model.Update;

/**
 * Интерфейс сервиса для выполнения действий на основе входящих обновлений от Telegram бота.
 * Определяет основной контракт для обработки сообщений и команд пользователя.
 */
public interface ExecuteService {

    /**
     * Обрабатывает входящее обновление от Telegram API.
     *
     * <p>Реализации этого метода должны содержать логику:
     * <ul>
     *   <li>Анализа типа полученного обновления (сообщение, команда и т.д.)</li>
     *   <li>Определения соответствующей обработки</li>
     *   <li>Выполнения необходимых действий</li>
     *   <li>Формирования и отправки ответа пользователю</li>
     * </ul>
     * </p>
     *
     * @param update объект {@link Update}, содержащий информацию о входящем сообщении/команде
     */
    void execute(Update update);
}
